package org.eclipse.winery.model.tosca.yaml.extensions;

import org.eclipse.winery.model.tosca.yaml.visitor.AbstractParameter;
import org.eclipse.winery.model.tosca.yaml.visitor.AbstractResult;
import org.eclipse.winery.model.tosca.yaml.visitor.IVisitor;

public class YOTPermutationMapping extends YOTPrmMapping {

    protected YOTPermutationMapping(Builder builder) {
        super(builder);
    }

    @Override
    public <R extends AbstractResult<R>, P extends AbstractParameter<P>> R accept(IVisitor<R, P> visitor, P parameter) {
        return visitor.visit(this, parameter);
    }

    public static class Builder extends YOTPrmMapping.Builder<Builder> {

        public YOTPermutationMapping build() {
            return new YOTPermutationMapping(this);
        }

        @Override
        public Builder self() {
            return this;
        }
    }
}
